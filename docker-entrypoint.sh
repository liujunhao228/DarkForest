#!/bin/sh
# ============================================================
# 黑暗森林 (Dark Forest) - 容器启动入口脚本
# 使用 migrate deploy 进行安全的生产环境数据库迁移
# ============================================================

set -e

echo "=========================================="
echo "  黑暗森林 - 容器启动"
echo "=========================================="

# 设置数据库 URL（如果未设置）
export DATABASE_URL=${DATABASE_URL:-"file:/app/db/custom.db"}

echo "数据库路径: $DATABASE_URL"

# -------------------- 数据库初始化 --------------------
echo ""
echo "正在执行数据库初始化..."

# 确保数据库目录存在
mkdir -p /app/db

# 先尝试标记迁移为已完成（适用于数据库已有数据的情况）
# 如果迁移记录已存在会报错，我们忽略这个错误
bunx prisma migrate resolve --applied 20260406000000_init 2>/dev/null || true

# 然后执行 migrate deploy（如果迁移已标记，这步会跳过）
bunx prisma migrate deploy

echo "数据库初始化完成！"

# -------------------- 启动应用 --------------------
echo ""
echo "启动应用服务器..."
exec "$@"
